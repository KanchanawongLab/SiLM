def getIndex(mat, wavelength):
    'get refractive index at specified wavelength'
    if mat == 'H2O':
        x = H2O_Wavelength
        y= H2O_n
    elif mat == 'Si':
        x = Si_Wavelength
        y = Si_n
    elif mat =='SiO2':
        x= SiO2_Wavelength
        y = SiO2_n
    elif mat =='BK7':
        x= BK7_wavelength
        y = BK7
    f = interpolate.interp1d(x,y)
    return np.float32(f(wavelength))